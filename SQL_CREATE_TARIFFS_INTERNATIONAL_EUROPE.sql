CREATE TABLE IF NOT EXISTS tariffs_international_europe (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  service_name text NOT NULL,
  country text NOT NULL,
  weight_from numeric NOT NULL DEFAULT 0,
  weight_to numeric,
  cost numeric NOT NULL,
  created_at timestamptz DEFAULT now()
);

-- Crear indice para optimizar busquedas
CREATE INDEX IF NOT EXISTS idx_intl_europe_tariffs_lookup
  ON tariffs_international_europe(service_name, country, weight_from);

-- Habilitar RLS
ALTER TABLE tariffs_international_europe ENABLE ROW LEVEL SECURITY;

-- Eliminar politicas existentes si las hay
DROP POLICY IF EXISTS "Authenticated users can read international europe tariffs" ON tariffs_international_europe;
